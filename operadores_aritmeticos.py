"""
* Adição: +
* Subtração: -
* Multiplicação: *
* Divisão: /
* Divisão inteira: //
* Módulo (obter o resto): %
* Exponeciação: **

Ordem dos operadores: 
1. Agrupamento -> ()
2. Exponeciação -> **
3. Multiplicação, divisão e o resto -> *, /, %
4. Adição e subtração -> *, -

Operadores de atribuição:
* += -> x += 3 -> x = x + 3
* -= -> x -= 3 -> x = x - 3
* *= -> x *= 3 -> x = x * 3
* //= -> x //= 3 -> x = x // 3
* %= -> x %= 3 -> x = x % 3
* **= -> x **= 3 -> x = x ** 3

"""
x = 1
x += 3 # 1 + 3 = 4
print(x)

