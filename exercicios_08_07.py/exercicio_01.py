# 01. Faça dois scripts que mostrem a mensagem "Olá mundo!" 10 vezes na tela. Utilize uma estrutura de repetição diferente em cada um dos scripts (for e while).


for i in range(10):
    print(f"Olá mundo! {i + 1}")         # FOR



i: int = 0
while i < 10:
    print(f"Olá mundo! {i + 1} vezes")   # WHILE
    i += 1


